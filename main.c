// Bootstrap processor starts running C code here.
// Allocate a real stack and switch to it, first
// doing some setup required for memory allocator to work.
int
main(void)
{
	char *p;
	u16int *mem1;
	u16int *mem2;
	u16int *com1;
	u16int com1port;
	u16int *com2;
	u16int com2port;
	u32int allocmem1;
	u32int curend;
	u32int *kernstart;

	mem1 = (void*)0x500;
	mem2 = (void*)0x504;
	com1 = (void*)0x508;
	com1port = *com1;
	com2 = (void*)0x50c;
	com2port = *com2;
	allocmem1 = lowmem = (*mem1)*1024;
	highmem = (*mem2)*(64*1024);
	totalmem = lowmem+highmem;

	singleuser = 1; // boot into single user mode initally
	booted = 0;
	syscons = 1; // cga/ega/vga is the system console
	sysuart = 1; // make the uart initally print messages

	cgamove(2, 0);
	cgaprintstr("xv6...\n");
	earlyuartprintstr("\nxv6...\n");
	cprintf("memory = %u bytes (low = %x, high = %x)\n", totalmem, lowmem, highmem);
	cprintf("kernel = %x (%u kb)\n",  V2P(end), ((uint)(end-KERNBASE))/1024);
	cprintf("serial = com0 = %x, com1 = %x\n", com1port, com2port);
	if(lowmem < 0x3e0000){ // found experimentally. you basically need 6 meg
		cli();
		cprintf("panic: not enough memory!");
		for(;;) ;
	}
	consoleinit1();   // I/O devices
	if(lowmem > 4*1024*1024){
		kinit1(end, P2V(4*1024*1024)); // phys page allocator
		allocmem1 -= 4*1024*1024;
	} else {
		cprintf("warning: low memory (%x < %x)\n", lowmem, 4*1024*1024);
		kinit1(end, P2V(lowmem));
		allocmem1 = 0;
	}
	//curend = 4*1024*1024;
	kvmalloc();      // kernel page table
	cprintf("starting vsd release 1\n");
	mpinit();        // collect info about this machine
	lapicinit();
	if(!ismp)
		ncpu = 1;
	cprintf("mpinit: found %d processors\n", ncpu);
	seginit();       // set up segments
	picinit();       // interrupt controller
	ioapicinit();    // another interrupt controller
	consoleinit2();   // I/O device's interrupts
	if(com1port > 0) // serial devices
		uartinit(com1port);
	if(com2port > 0)
		uartinit(com2port);
	pinit();         // process table
	tvinit();        // trap vectors
	if(!ismp){
		cprintf("cpu%d: not using mp kernel\n", cpu->id);
		timerinit();   // uniprocessor timer
	}
	startothers();   // start other processors
	// free the memory so the kernel knows about it
	if(allocmem1 > 0){
		kinit2(P2V(4*1024*1024), P2V(lowmem));
		cprintf("cpu%d: low start = %x, low end = %x\n", cpu->id, 4*1024*1024, lowmem);
	}
	if(highmem > 0){
		if(highmem > PHYSTOP){
			kinit2(P2V(ISAHOLEEND), P2V(PHYSTOP));
			cprintf("cpu%d: warning: highmem > PHYSTOP\n", cpu->id);
			cprintf("cpu%d: high start = %x, high end = %x\n", cpu->id, ISAHOLEEND, PHYSTOP);
		} else {
			kinit2(P2V(ISAHOLEEND), P2V(highmem));
			cprintf("cpu%d: high start = %x, high end = %x\n", cpu->id, ISAHOLEEND, highmem);
		}
	}
	ideinit();       // disk
	binit();         // buffer cache and /dev/disk*
	fileinit();      // file tables
	bitbucket_init(); // /dev/null, /dev/zero, /dev/robpike devices
	sysctl_init();  // /dev/sysctl
	sysname_init(); // /dev/sysname
	kernelinit();	// kernel process
	userinit();      // first user process
	// Finish setting up this processor in mpmain.
	cprintf("cpu%d: starting timesharing\n", cpu->id);
	mpmain();
}

// Other CPUs jump here from entryother.S.
static void
mpenter(void)
{
	switchkvm(); 
	seginit();
	lapicinit();
	mpmain();
}

// Common CPU setup code.
static void
mpmain(void)
{
	idtinit();       // load idt register
	xchg(&cpu->started, 1); // tell startothers() we're up
	scheduler();     // start running processes
}

pde_t entrypgdir[];  // For entry.S

// Start the non-boot (AP) processors.
static void
startothers(void)
{
	extern uchar _binary_entryother_start[], _binary_entryother_size[];
	uchar *code;
	struct cpu *c;
	char *stack;

	// Write entry code to unused memory at 0x7000.
	// The linker has placed the image of entryother.S in
	// _binary_entryother_start.
	code = p2v(0x7000);
	memmove(code, _binary_entryother_start, (uint)_binary_entryother_size);

	for(c = cpus; c < cpus+ncpu; c++){
		if(c == cpus+cpunum())  // We've started already.
			continue;

		// Tell entryother.S what stack to use, where to enter, and what 
		// pgdir to use. We cannot use kpgdir yet, because the AP processor
		// is running in low  memory, so we use entrypgdir for the APs too.
		stack = kalloc();
		*(void**)(code-4) = stack + KSTACKSIZE;
		*(void**)(code-8) = mpenter;
		*(int**)(code-12) = (void *) v2p(entrypgdir);

		lapicstartap(c->id, v2p(code));

		// wait for cpu to finish mpmain()
		while(c->started == 0)
			;
	}
}

